    private void toleranciaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_toleranciaKeyTyped
        // TODO add your handling code here:
           Character c = evt.getKeyChar();

        if (!Character.isDigit(c)) {
            if (c != '.') {
                evt.consume();
            }

            if (c == '.') {
                if (this.tolerancia.getText().indexOf('.') != -1) {
                    evt.consume();
                }
            }
        }
    }//GEN-LAST:event_toleranciaKeyTyped

    private void txtintervaloaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtintervaloaKeyTyped
        // TODO add your handling code here:
                        Character c = evt.getKeyChar();

        if (!Character.isDigit(c)) {
            if (c < '-' || c > '.') {
                evt.consume();
            }

            if (c == '.') {
                if (this.txtintervaloa.getText().indexOf('.') != -1) {
                    evt.consume();
                }
            }

            if (c == '-') {
                if (this.txtintervaloa.getText().indexOf('-') != -1) {
                    evt.consume();
                }
            }
        }

    }//GEN-LAST:event_txtintervaloaKeyTyped

